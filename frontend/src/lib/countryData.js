// Kenya as default
export const DEFAULT_COUNTRY = countries.find((c) => c.code === "KE");

// Find country by dial code
export const findCountryByDialCode = (dialCode) => {
  return countries.find((c) => c.dialCode === dialCode);
};

// Find country by code
export const findCountryByCode = (code) => {
  return countries.find((c) => c.code === code);
};

// Parse phone number to extract country code and local number
export const parsePhoneNumber = (phoneNumber) => {
  if (!phoneNumber || !phoneNumber.startsWith("+")) {
    return { country: DEFAULT_COUNTRY, localNumber: phoneNumber || "" };
  }

  // Try to match the dial code (longest match first)
  const sortedCountries = [...countries].sort(
    (a, b) => b.dialCode.length - a.dialCode.length
  );

  for (const country of sortedCountries) {
    if (phoneNumber.startsWith(country.dialCode)) {
      return {
        country,
        localNumber: phoneNumber.slice(country.dialCode.length),
      };
    }
  }

  return { country: DEFAULT_COUNTRY, localNumber: phoneNumber };
};

// Format phone number with country code
export const formatPhoneNumber = (country, localNumber) => {
  const cleanNumber = localNumber.replace(/\D/g, "");
  return `${country.dialCode}${cleanNumber}`;
};
